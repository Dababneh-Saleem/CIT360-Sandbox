                        <h1>Education History</h1>
                        
                        <h5>ELEMENTARY SCHOOL YEARS</h5>
                            <p>
                                Growing up in London, Ontario Canada, most of my education was completed there. I started out at a school called <a href='http://www.tvdsb.ca/SirGeorges.cfm' target="_blank" title="Cartier Public School"><u>Cartier Public</u></a> School where 
                                I attended kindergarten and first grade.  This school was located less than 100 yards from our home and while we started attending <a href='http://www.ldcsb.on.ca/schools/ros/Pages/default.aspx' target="_blank" title="Holy Rosary Catholic School"><u>Holy Rosary </u></a>
                                Catholic School we still played here at Cartier after school almost everyday until I left Canada.  Cartier was a good school but our parents were 
                                interested in getting us into schools found in the Catholic School District.  While attending Holy Rosary my teachers noticed that I needed to be
                                challenged more so they encouraged me to try out for St. Mary's Catholic School which is a school for advanced learning and performing arts.
                                I auditioned, got accepted and moved on to St.Mary's where I attended school from 5th through 8th grade.<br />
                               <br />
                               <a href='http://www.ldcsb.on.ca/schools/MAL/Pages/default.aspx' target="_blank" title="St.Mary's Catholic School"><u>St. Mary's</u></a>  Catholic School was a very special school and a place that really helped to bring my talents and abilities to light.  The school had a
                               renowned Choir & Orchestra program and was also known for more challenging course work as well. At St.Mary's the expectations were high.
                               We had no time for slacking off.  We had a full schedule of studies coupled with a seemingly full-time gig singing and performing in our
                               orchestra.  We traveled throughout the city of London performing in various cathedrals and music halls.  The school had a history of 
                               winning music competitions dating back over a decade and we were drilled to compete at the highest levels.  We also put on an annual
                               musical a the University of Western Ontario which was a lot of work.  One year the school put on the musical 'Joseph and the Amazing 
                               Technicolor Dreamcoat' where Donny Osmond joined in to played Joseph. During my time at St.Mary's I learned to play the Viola and the
                               Double Bass while also being trained vocally by arguably one of the Regions best vocal directors, William 'Bill' Zadorsky.  
                            </p>
                            <br />
                            <h5>HIGH SCHOOL YEARS</h5>
                            <p>
                                After completing four amazing years at St.Mary's I had to make a decision to continue moving forward with many of my classmates or to 
                                chart a new path.  It was either, continue spending an hour each morning and afternoon riding city buses to attend Catholic Central High 
                                or go to Regina Mundi to rejoin my brothers at the local High School.  While it was a difficult choice, I decided to get back to my local
                                school district and attend <a href='http://www.ldcsb.on.ca/schools/rmc/Pages/default.aspx' target="_blank" title="Regina Mundi High School"><u>Regina Mundi</u></a> High School. My two older brothers had attended here and I felt like it was time for a change anyway.
                                I attended Regina Mundi for 3 full years, from my freshman to my junior year of high school before moving to the United States. I was very 
                                active in the school and had the opportunity to participate in sports (football, basketball, track and field) while also performing in the 
                                High School choir and the band.  Luckily I was athletic enough to pull this off, many of the jocks poked fun at the choir and band stuff I 
                                did but I ended up earning the job as the team quarterback so that seemed to prevent them from picking on me too much (it still happened).
                               <br /><br />
                               The summer before my senior year of high school I moved from Canada to Phoenix, Arizona.  More specifically, I moved to Gilbert, AZ where I  
                               enrolled at <a href='http://www.gilbertschools.net/Domain/11' target="_blank" title="Gilbert High School "><u>Gilbert High School</u></a> for my senior year.  This was an exciting time in my life as there were many changes going on.  I enjoyed 
                               my senior year of high school more than any other year mainly because I got a fresh start there.  I wasn't the dorky kid everyone knew 
                               growing up, I had matured quite a bit and this year proved to be a time where my confidence would grow significantly.  It is also the year
                               that lead me to two very influential people in my life.  You can read more about them at the 
                               <a href="/people/influentialpeople.php" title="Influential People In My Life"><u>Influential People</u></a> page of my site.  
                            </p>

