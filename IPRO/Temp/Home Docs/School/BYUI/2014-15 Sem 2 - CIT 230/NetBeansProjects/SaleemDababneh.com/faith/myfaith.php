                        <h1>My Faith</h1>
                            <p>
                                My faith has been a significant factor in my life, without it I know that I could not have been so successful in navigating the trials of my
                                life.  As I look back on where I have been and what I have been through I am reminded of how closely God's hand has been guiding my life.
                                <br /><br />
                                I was raised by a very faithful mother who made sure that we not only attended church every Sunday, but that we served in the church as well.
                                From as early as we could we all were given the opportunity to serve as Alter Boys at church.  We attended Holy Rosary church from as far back
                                as I can remember.  When we lived in Detroit we attended Holy Redeemer church and my brothers also attended school there before we moved 
                                to Canada.  In Canada, we attended Holy Rosary school even though it was 5 miles further from our home than the local public school.
                                <br /><br />
                                We were taught about Jesus Christ at school and at church which was a real blessing.  I was able to study the life of Christ from the Bible
                                during the school week and then see and hear the priest relate those stories to our daily lives.  We had a really nice priest for many years
                                named Father Hennessey.  He was a very kind old man and he was always so happy to see us. While some found him to be older and not as hip, I 
                                really payed close attention when he spoke and I learned much from him.  The church brought in a more energetic priest, a priest who was easily
                                30 years younger than Father Hennessey.  I liked him as well and he was a great story teller, also comparing the parables of the Bible to our day
                                but I didn't feel that he was as sincere as Father Hennessey.  I still listened and pondered the things shared but I found that I preferred mass 
                                when Father Hennessey was leading.  
                                <br /><br />
                                After a few years at Holy Rosary school I moved on to St.Mary's (you can read about this in the 'Education & Career' section of the site) and was 
                                provided a great opportunity to learn how to sing and perform.  My favorite pieces that we studied were the songs of faith.  I always felt so much
                                closer to Heaven when we sang, especially when we got to sing in the Cathedral where the sound resonated so beautifully. It was this music that
                                helped me through some of the harder times in my life and I will be forever grateful that I had this great experience at St.Mary's. My experience
                                here lead me to decide to attend Regina Mundi High School where I continued to learn more about the life of the Savior.  
                                <br /><br />
                                One semester, during my sophomore year of high school, we were given a semester wide study assignment to study different religions of the world. I 
                                put a lot of time and effort into this assignment and researched several different faiths.  I had a real desire to know God and to know how I could 
                                best live the way He would expect me to.  At the end of the school year I was awarded with a school-wide award, the Religious Studies Award for my
                                work.  This acknowledgment and recognition drove me to continue to pursue this research on my own throughout the summer and into the next year. It was
                                during the course of the next school year that I met a friend who was a member of the Church of Jesus Christ of Latter-Day Saints.  He was the only
                                guy in our entire high school of his faith but somehow we crossed-paths and this altered the course of my life forever.  
                                <br /><br />
                                Through my own studies and prayer about the Church of Jesus Christ of Latter-Day Saints I came to a better understanding of my purpose in this life
                                and of God's plan for all of His children.  I chose to be baptized six months after my sixteenth birthday in the same manner that Christ was, by 
                                full immersion.  I have come to know for myself that God does indeed live, that He loves each and everyone of us and that He has prepared a way, 
                                through Christ our Savior, for us to return to live with Him someday.  I have also come to know for myself, through the Holy Ghost, that I can be 
                                with my family, even after death, as a family unit, that death does not break the eternal bonds of marriage nor does it take away our rights and 
                                privileges as families.  I know that it is by His infinite wisdom that we are all here on this earth, to learn to choose for ourselves to follow 
                                His ways, to better ourselves and to claim the blessings of the Atonement of Christ through choosing to follow His gospel and His ways.  
                                <br /><br />
                                My life has been greatly blessed through receiving my own personal witness of these things and it is by the strength of that personal witness that
                                I can do everything I do. If I can leave anything to my family and loved ones it would be the knowledge I have received from my life journey that 
                                lead me to this personal testimony.
                                <br /><br />
                                
