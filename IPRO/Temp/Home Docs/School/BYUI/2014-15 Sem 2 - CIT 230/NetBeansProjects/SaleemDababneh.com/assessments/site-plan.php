                            <h3>Typography</h3>
                            <p>
                                Typography Selections - Target Areas for use<br />
                                <br />
                                Arial, Verdana, sans-serif - General written text, sub-headings<br />
                                Lovelo Black, Arial, Verdana, sans-serif - Headings, Titles, navigation links<br />
                                Lovelo Black, Arial, Verdana, sans-serif – ALL CAPS	MENU ITEMS<br />
                                <br />
                                In addition to the above selected fonts, I will use varying levels of font size in the menus, links, headings 
                                and text but I will keep to limited color contrasts in text font to keep the text clear and to avoid distractions.
                                Font sizes will be set at a 16 point font but will scale by percentages of up to 175% and down to 90%.  <br />
                            </p>
                            <br />
                            <h3>Navigation</h3>
                            <p>
                                Navigation will flow from top left to bottom right.  The main text of any given page will be left-aligned but may 
                                shift to either side to allow for a joining flow from related photos.  The top of each page will include a colored 
                                header containing a family photo that will act as a 'Home Page' link.  The main body of text will have images 
                                scattered throughout.  All primary links will be found on the top, center portion of the site and the bottom 
                                of the site will contain links for secondary links such as site plan, contact info etc.  <br />
                            </p>
