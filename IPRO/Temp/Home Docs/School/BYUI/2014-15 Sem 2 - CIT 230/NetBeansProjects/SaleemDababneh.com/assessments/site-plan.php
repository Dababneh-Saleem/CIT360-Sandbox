                                Color will be used primarily in the header box that will span the width of the page and make up the navigation 
                                panel and title box.  There will also be a use of white-space in the main regions for ease of reading.  
                                Text colors will be black unless a background color is used for a specific section, then white would be used. 
                                Photos will be strategically placed in a position of unison with the related text on the relative page so as 
