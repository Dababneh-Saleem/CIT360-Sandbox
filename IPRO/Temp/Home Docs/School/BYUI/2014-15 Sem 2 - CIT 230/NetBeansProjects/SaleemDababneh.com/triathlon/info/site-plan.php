                            <h3>Site Color Scheme</h3>
                                <p>
                                Black will be used primarily in the header box that will span the width of the page and make up the navigation 
                                panel and title box.  There will then be a use of white-space in the main regions for ease of reading.  
                                Text colors will be black unless a background color is used for a specific section, then white would be used. 
                                Photos will be strategically placed in a position of unison with the related text on the relative page so as 
                                to aid in the flow of viewing and navigation.  The footer will utilize a burnt orange (HEX #FF831C) that 
                                will span from left to right and will cover and wrap the footer links and legal statements.<br />
                            </p>
                            <br />
                            <h3>Typography</h3>
                            <p>
                                Typography Selections - Target Areas for use<br />
                                <br />
                                Arial, Verdana, sans-serif - General written text, sub-headings<br />
                                Arkhip, Arial, Verdana, sans-serif - Headings, Titles, navigation links<br />
                                Arkhip, Arial, Verdana, sans-serif – ALL CAPS	MENU ITEMS<br />
                                <br />
                                In addition to the above selected fonts, I will use varying levels of font size in the menus, links, headings 
                                and text but I will keep to limited color contrasts in text font to keep the text clear and to avoid distractions.
                                Font sizes will be set at a 16 point font but will scale by percentages of up to 175% and down to 75%.  <br />
                            </p>
                            <br />
                            <h3>Navigation</h3>
                            <p>
                                Navigation will flow from top left to bottom right.  The main text of any given page will be left-aligned but may 
                                shift to either side to allow for a joining flow from related photos.  The top of each page will include a colored 
                                header containing the Triathlon Logo image that will act as a 'Home Page' link.  The main body of text will have images 
                                scattered throughout.  All primary links will be found on the top, center portion of the site and the bottom 
                                of the site will contain links for secondary links such as the site plan, contact info, and site resources page.<br />
                            </p>
