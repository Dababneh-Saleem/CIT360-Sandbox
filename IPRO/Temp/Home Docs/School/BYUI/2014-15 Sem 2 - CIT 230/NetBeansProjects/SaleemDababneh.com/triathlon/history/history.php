                            <h4>
                                The San Quentin prison is situated on prime real estate overlooking the San Francisco Bay. The facility is almost 
                                150 years old and some would like to see it retired and the land used for housing. Others would like to see the 
                                prison turned into a historic site and made untouchable by developers. Even though this prison may eventually close, 
                                it will always remain a colorful part of California's, and America's, past.<br />
                                <br />
                                The discovery of gold at Sutter's Mill on January 24, 1848 impacted all aspects of life in California. The 
                                gold meant a great influx of new people to the region. Unfortunately, the gold rush also brought a number 
                                of unsavory people. Many of these would eventually require incarceration. These circumstances led to the 
                                creation of one of the most famous prisons in the nation.<br />
                                <br />
                                Before a permanent prison facility was erected in California, convicts were housed on prison ships. The 
                                use of prison ships as a means to hold those guilty of crimes was not new to the penitentiary system. The 
                                British held many patriots on prison ships during the American Revolution. Even years after numerous permanent 
                                facilities existed, this practice continued in a more tragic fashion during World War II. The Japanese 
                                transported a number of prisoners in merchant vessels that were unfortunately the targets of many allied naval 
                                ships. <br />
                                <br />
                                Before San Quentin was built on the outskirts of San Francisco, the prisoners were kept on prison ships such 
                                as the "Waban." The California legal system decided to create a more permanent structure because of overcrowding 
                                and frequent escapes aboard the ship. They chose Point San Quentin and purchased 20 acres of land to begin what 
                                would become the state's oldest prison: San Quentin. The construction of the facility began in 1852 with the 
                                use of prison labor and ended in 1854. The prison has had a storied past and continues to operate today. <br />
                                <br />
                                The prison is situated on prime real estate overlooking the San Francisco Bay. The facility is almost 150 years 
                                old and some would like to see it retired and the land used for housing. Others would like to see the prison 
                                turned into a historic site and made untouchable by developers. Even though this prison may eventually close, 
                                it will always remain a colorful part of California's, and America's, past. <br />                                
