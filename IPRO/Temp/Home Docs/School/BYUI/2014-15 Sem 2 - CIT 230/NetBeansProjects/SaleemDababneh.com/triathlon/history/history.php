                            <h4>
                                Born of necessity, perhaps even political expediency, Alcatraz represents the federal government's 
                                response to post-Prohibition, post-Depression America. Both the institution and the men confined 
                                within its walls are a part of this era, and in order to be studied with any degree of understanding, 
                                it must be attended to with a focus on this time period. Prisons are a reflection of society and the 
                                reflection offered by Alcatraz is one of great clarity. <br />
                                <br />
                                The collaborative effort of attorney general Homer Cummings and Director of the Bureau of Prisons, 
                                Sanford Bates, produced a legendary prison that seemed both necessary and appropriate to the times. 
                                The emergence of persistent assertions about J. Edgar Hoover's interest and influence with regard to 
                                Alcatraz cannot be corroborated, but neither have they been completely denied. With the public peace 
                                constantly threatened by crime, a response had to be made and Alcatraz was that response. <br />
                                <br />
                                An in-house memo issued by Cummings shortly after taking office addressed the subject of creating a 
                                special prison for kidnapers, racketeers, and individuals guilty of predatory crimes. A remote site 
                                was sought, one that would prohibit constant communication with the outside world by those confined 
                                within its walls. Although land in Alaska was being considered, the availability of Alcatraz Island 
                                conveniently coincided with the government's perceived need for a super-prison. <br />
                                <br />
                                Having taken possession of the former Army prison and having circumvented the San Francisco citizens 
                                who were concerned at the prospect of vicious criminals in the near vicinity, the Bureau of Prisons 
                                set about selecting a warden who could do the job. A well-organized, no-nonsense businessman and 
                                prison administrator with twelve years of experience in the California Department of Corrections, 
                                James A. Johnston was to be that man. Johnston had retired at the time of his appointment by the 
                                Department of Justice, and its acceptance resulted in his serving as warden of Alcatraz for the 
                                next fourteen years.<br />
                                <br />
                                Classified as a concentration model, where difficult-to-manage prisoners from other institutions 
                                would be concentrated under one roof, Alcatraz served as an experiment. Segregation on this scale 
                                had not before been practiced, and only time would indicate its success or failure. Warden Johnston 
                                and the second Director of the Bureau of Prisons, James V. Bennett, both were men well ahead of their 
                                time. Visionaries in the field of penology, their knowledge enabled Alcatraz to function as it had 
                                been hoped and to serve later as a model for the federal prison located in Marion, Illinois.<br />
                            </h4>
