                               enrolled at <a href='http://www.gilbertschools.net/Domain/11' target="_blank" title="Gilbert High School "><u>Gilbert High School</u></a> for my senior year.  This was an exciting time in my life as there were many changes going on.  I enjoyed <br />
                               my senior year of high school more than any other year mainly because I got a fresh start there.  I wasn't the dorky kid everyone knew <br />
                               growing up, I had matured quite a bit and this year proved to be a time where my confidence would grow significantly.  It is also the year<br />
