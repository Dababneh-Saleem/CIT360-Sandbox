                        <h1>Education History</h1>
                        
                        <h5>ELEMENTARY SCHOOL YEARS</h5>
                            <p>
                                Growing up in London, Ontario Canada, most of my education was completed there. I started out at a school called <a href='http://www.tvdsb.ca/SirGeorges.cfm' target="_blank" title="Cartier Public School"><u>Cartier Public</u></a> School where <br />
                                I attended kindergarten and first grade.  This school was located less than 100 yards from our home and while we started attending <a href='http://www.ldcsb.on.ca/schools/ros/Pages/default.aspx' target="_blank" title="Holy Rosary Catholic School"><u>Holy Rosary </u></a><br />
                                Catholic School we still played here at Cartier after school almost everyday until I left Canada.  Cartier was a good school but our parents were <br />
                                interested in getting us into schools found in the Catholic School District.  While attending Holy Rosary my teachers noticed that I needed to be<br />
                                challenged more so they encouraged me to try out for St. Mary's Catholic School which is a school for advanced learning and performing arts.<br />
                                I auditioned, got accepted and moved on to St.Mary's where I attended school from 5th through 8th grade.<br />
                               <br />
                               <a href='http://www.ldcsb.on.ca/schools/MAL/Pages/default.aspx' target="_blank" title="St.Mary's Catholic School"><u>St. Mary's</u></a>  Catholic School was a very special school and a place that really helped to bring my talents and abilities to light.  The school had a<br />
                               renowned Choir & Orchestra program and was also known for more challenging course work as well. At St.Mary's the expectations were high.<br />
                               We had no time for slacking off.  We had a full schedule of studies coupled with a seemingly full-time gig singing and performing in our<br />
                               orchestra.  We traveled throughout the city of London performing in various cathedrals and music halls.  The school had a history of <br />
                               winning music competitions dating back over a decade and we were drilled to compete at the highest levels.  We also put on an annual<br />
                               musical a the University of Western Ontario which was a lot of work.  One year the school put on the musical 'Joseph and the Amazing <br />
                               Technicolor Dreamcoat' where Donny Osmond joined in to played Joseph. During my time at St.Mary's I learned to play the Viola and the<br />
                               Double Bass while also being trained vocally by arguably one of the Regions best vocal directors, William 'Bill' Zadorsky.<br />  
                            </p>
                            <br />
                            <h5>HIGH SCHOOL YEARS</h5>
                            <p>
                                After completing four amazing years at St.Mary's I had to make a decision to continue moving forward with many of my classmates or to <br />
                                chart a new path.  It was either, continue spending an hour each morning and afternoon riding city buses to attend Catholic Central High <br />
                                or go to Regina Mundi to rejoin my brothers at the local High School.  While it was a difficult choice, I decided to get back to my local<br />
                                school district and attend <a href='http://www.ldcsb.on.ca/schools/rmc/Pages/default.aspx' target="_blank" title="Regina Mundi High School"><u>Regina Mundi</u></a> High School. My two older brothers had attended here and I felt like it was time for a change anyway.<br />
                                I attended Regina Mundi for 3 full years, from my freshman to my junior year of high school before moving to the United States. I was very <br />
                                active in the school and had the opportunity to participate in sports (football, basketball, track and field) while also performing in the <br />
                                High School choir and the band.  Luckily I was athletic enough to pull this off, many of the jocks poked fun at the choir and band stuff I <br />
                                did but I ended up earning the job as the team quarterback so that seemed to prevent them from picking on me too much (it still happened).<br />
                               <br />
